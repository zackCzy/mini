package cn.mini.domain;

public class Word {
  private String name,worde,usap,ukp;

public Word() {}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

public String getWorde() {
	return worde;
}

public void setWorde(String worde) {
	this.worde = worde;
}

public String getUsap() {
	return usap;
}

public void setUsap(String usap) {
	this.usap = usap;
}

public String getUkp() {
	return ukp;
}

public void setUkp(String ukp) {
	this.ukp = ukp;
}
}
